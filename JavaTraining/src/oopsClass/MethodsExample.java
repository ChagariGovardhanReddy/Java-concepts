package oopsClass;

public class MethodsExample {
	/*
	 * methods block or group of statements which will perform certain task. we have
	 * to call the method though object.
	 */

	// No parameter no return value
	void m1() {
		System.out.println("Java Trs");
	}

	// No parameters with return value
	String m2() {
		return ("dog ganga");

	}

	// with parameters no return value
	void m3(int a, int b) {
		System.out.println(a + b);

	}

	// with parameters with return value
	double m4(double c, double d) {
		return (c * d);

	}

}
